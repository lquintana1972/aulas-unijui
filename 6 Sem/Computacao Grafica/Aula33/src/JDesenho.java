private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
    Graphics g = this.getGraphics();
    g.drawRoundRect(evt.getX(), evt.getY(), 2, 2, 0, 0);
    
    if (pontos.size()>0 && Math.abs(pontos.get(0).x-evt.getX())<10
            && Math.abs(pontos.get(0).y-evt.getY())<10) {
        desenhaQuadrado();
    } else {
        pontos.add(evt.getPoint());
        desenhaLinhas();
    }


}//GEN-LAST:event_formMouseClicked

