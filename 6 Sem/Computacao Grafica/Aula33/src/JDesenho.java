    private void desenhaQuadrado() {
        if (pontos.size() > 1) {
            Graphics g = this.getGraphics();
            int[] xs = new int[pontos.size()];
            int[] ys = new int[pontos.size()];
            for (int i = 0; i < pontos.size(); i++) {
                Point point = pontos.get(i);
                xs[i] = new Double(point.getX()).intValue();
                ys[i] = new Double(point.getY()).intValue();
            }
            g.fillPolygon(new Polygon(xs, ys, pontos.size()));
            g.setColor(Color.BLUE);
            g.drawString("Fernando", (xs[0]+xs[1])/2+5, (ys[0]+ys[1])/2+5);
            pontos.clear();
        }
    }

    private void desenhaLinhas() {
        if (pontos.size() > 1) {
            Graphics g = this.getGraphics();
            Point ant = pontos.get(pontos.size() - 1);
            Point point = pontos.get(pontos.size() - 2);
            g.drawLine(point.x, point.y, ant.x, ant.y);
        }
    }

